export const flatStrings: Record<Locale, Record<string, string>> = {
  sw: flatten(swTree),
  en: flatten(enTree),
};

export type TKey = keyof typeof flatStrings.sw;

export function translate(
  locale: Locale,
  key: TKey,
  vars?: Record<string, string | number>,
): string {
  let text = flatStrings[locale][key] ?? flatStrings.sw[key] ?? String(key);
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replaceAll(`{${k}}`, String(v));
    }
  }
  return text;
}
